package day20;

public class WhileLoop2 {
	public static void main(String[] args) {
		
		
		int count = 1;
		while (count<=10){
			System.out.print(count+" ");
		count++;
		}
		System.out.println();
		System.out.println("count: "+count);
		System.out.println("-----");
		
		int index = 10;
		while (index>=1) {
			System.out.print(index+ " ");
			index--;
		}
		System.out.println();
		System.out.println("index " +index);
		
	}

}
